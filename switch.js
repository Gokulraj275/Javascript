var grade = 'A';

//console.log("Entering switch block<br/>");

switch(grade){
    case 'A': 
        console.log("Today is Monday");
        break;
    case 'B': 
        console.log("Today is Tuesday");
        break;
    case 'C': 
        console.log("Today is Wednesday");
        break;
    case 'D':
        console.log("Today is Thursday");
        break;
    default:
        console.log("Day Not Possible")
}